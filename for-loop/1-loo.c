//for loop
//sum of numbers from 1 to 100

#include <stdio.h>

int main()

{

	int sum = 0;

	int i;

	for(int i = 1; i <= 100; i++);

	{

		int sum = sum + i;

	}

		printf("%d\n", sum);

	return 0;

}

